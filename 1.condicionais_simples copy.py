# Condicioanis símples.

idade = 20

if idade >= 18:
    print("Você é maior de idade.")

# a condição (idade > = 18) é avaliada.

# se a idade for maior ou igual que 18, a condição é VERDADEIRA. 

# se na variável (idade fosse = 17), a condição seria FALSA e nada seria exibido.

# E por quê? Porque eu determinei o valor como 20, 
# 20 é maior (>) que 18 e 17 é menor (<), 
# e não há nenhuma condição que exiba dizendo que o usuário é menor de idade,
# mas veremos isso na condição composta.  